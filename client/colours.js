function getHexValue(id){
	if(id == "RR"){
		return "#C80000"
	} else if(id == "GG"){
		return "#00C800";
	} else if(id == "BB"){
		return "#0000C8";
	}  else if(id == "YY"){
		return "#C8C800";
	} else if(id == "MM"){
		return "#C800C8";
	} else if(id == "OO"){
		return "#FF8000";
	} else if(id == "PP"){
		return "#FF33FF";
	} else if(id == "AA"){
		return "#00FFFF";
	} else if(id == "R"){
		return "#FF0000";
	} else if(id == "G"){
		return "#00FF00";
	} else if(id == "B"){
		return "#0000FF";
	} else if(id == "Y"){
		return "#FFFF00";
	} else if(id == "M"){
		return "#FF00FF";
	} else if(id == "O"){
		return "#FF9933";
	} else if(id == "P"){
		return "#FF66FF";
	} else if(id == "A"){
		return "#78FFFF";
	} else{
		return "#FFFFFF";
	}
}